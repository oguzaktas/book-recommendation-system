    private void text_idKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_text_idKeyPressed
        text_id.setEditable(true);
        int key = evt.getKeyCode();
        if ((key >= evt.VK_0 && key <= evt.VK_9) || (key >= evt.VK_NUMPAD0 && key <= evt.VK_NUMPAD9) || key == KeyEvent.VK_BACKSPACE || (key == KeyEvent.VK_ALT || key == KeyEvent.VK_TAB || key == KeyEvent.VK_ENTER || key == KeyEvent.VK_LEFT || key == KeyEvent.VK_RIGHT)) {
            text_id.setEditable(true);
            text_id.setBackground(Color.YELLOW);
        } else {
            text_id.setEditable(false);
            text_id.setBackground(Color.RED);
        }
    }//GEN-LAST:event_text_idKeyPressed

