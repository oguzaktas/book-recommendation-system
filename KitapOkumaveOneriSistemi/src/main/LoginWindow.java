    private void Btn_GirisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_GirisActionPerformed
        if (checkInputs()) {
            Connection conn = null;
            try {
                conn = getConnection();
            } catch (SQLException | ClassNotFoundException ex) {
                Logger.getLogger(LoginWindow.class.getName()).log(Level.SEVERE, null, ex);
            }
            PreparedStatement ps;
            ResultSet rs;
            String query = "SELECT * FROM `BX-Users` WHERE `User-ID` = ? AND Password = ?;";
            String pass = String.valueOf(password.getPassword());
            try {
                ps = conn.prepareStatement(query);
                ps.setString(1, text_id.getText());
                ps.setString(2, pass);
                rs = ps.executeQuery();
                if (rs.next()) {
                    this.dispose();
                    new UserWindow(text_id.getText()).setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(null, "<html><b>Kullanici ID veya sifreniz hatalidir.</b></html>");
                }
            } catch (SQLException ex) {
                Logger.getLogger(LoginWindow.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "<html><b>Uye girisi islemi sirasinda hata olustu.</b></html>");
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(LoginWindow.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "<html><b>Bir ya da daha fazla alani bos biraktiniz.</b></html>");
        }
    }//GEN-LAST:event_Btn_GirisActionPerformed

